swap12([X, Y | Z], [Y, X | Z]).

%?- swap12([], []).
%@ false.

%?- swap12([1], [1]).
%@ false.

%?- swap12([1, 2], [1, 2]).
%@ false.

%?- swap12([1, 2], [2, 1]).
%@ true.

%?- swap12([2, 1], [1, 2]).
%@ true.

%?- swap12([1, 2, 3], [2, 1]).
%@ false.

%?- swap12([1, 2], [2, 1, 3]).
%@ false.

%?- swap12([1, 2, 3], [2, 1, 3]).
%@ true.
